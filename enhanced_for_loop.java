// enhanced for loop  -> only works with array and array type of data and collections 
public class enhanced_for_loop {
    
public static void main(String[] args) {
    
    int nums[] =  new int[4];

    nums[0] = 1;
    nums[1] = 2;
    nums[2] = 3;
    nums[3] = 4;


for(int n : nums){

    System.out.println(n);

}

}

}
